package 매일코딩;

import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStreamReader;
import java.util.StringTokenizer;

public class 복습2_주사위굴리기 {
	static int N, M;
	static int x,y; // 주사위 초기 좌표
	static int k; // 주사위 이동 횟수
	static int[][] map;
	static int[] dice = new int[7];
	
	static int[] dr = {0,0,-1,1}; // 동 서 북 남
	static int[] dc = {1,-1,0,0}; 
	
	public static void main(String[] args) throws IOException{
		BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
		StringTokenizer st = new StringTokenizer(br.readLine());
		
		N = Integer.parseInt(st.nextToken());
		M = Integer.parseInt(st.nextToken());
		x = Integer.parseInt(st.nextToken());
		y = Integer.parseInt(st.nextToken());
		k = Integer.parseInt(st.nextToken());
		
		map = new int[N][M];
		
		for(int i=0; i<N; i++) {
			st = new StringTokenizer(br.readLine());
			for(int j=0; j<M; j++) {
				map[i][j] = Integer.parseInt(st.nextToken());
			}
		}
		
		st = new StringTokenizer(br.readLine());
		for(int i=0; i<k; i++) {
			int dir = Integer.parseInt(st.nextToken());
			
			int nx = x + dr[dir-1];
			int ny = y + dc[dir-1];
			
			if(nx >= 0 && ny >= 0 && nx < N && ny < M) {
				changeDice(dir);
				
				if(map[nx][ny] == 0) { // 해당 칸이 0이라면 
					map[nx][ny] = dice[6]; // 해당 칸에 주사위에 적힌 숫자로 변경
				}else { 			// 해당 칸이 0이 아니면
					dice[6] = map[nx][ny]; // 주사위에 적힌 숫자를 해당칸에 적힌 숫자로 변경
					map[nx][ny] = 0; // 해당 칸은 0으로 변경
				}
				
				x = nx;
				y = ny;
				System.out.println(dice[1]); // 윗면에 적힌 숫자 출력
			}			
		}
	}
		
		public static void changeDice(int d) {
			int[] temp = dice.clone();
			// 6 밑면, 1 윗면
			// 동쪽은 1, 서쪽은 2, 북쪽은 3, 남쪽은 4
			if(d == 1) { // 동쪽 1
				dice[1] = temp[4];
				dice[3] = temp[1];
				dice[4] = temp[6];
				dice[6] = temp[3];
			}else if(d == 2) { // 서쪽 2
				dice[1] = temp[3];
				dice[3] = temp[6];
				dice[4] = temp[1];
				dice[6] = temp[4];
			}else if(d == 3) { // 북쪽 3
				dice[1] = temp[5];
				dice[2] = temp[1];
				dice[5] = temp[6];
				dice[6] = temp[2];
			}else if(d == 4) { // 남쪽 4
				dice[1] = temp[2];
				dice[2] = temp[6];
				dice[5] = temp[1];
				dice[6] = temp[5];
			}
		}
	}

