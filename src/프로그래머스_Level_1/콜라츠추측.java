package 프로그래머스_Level_1;
/*
 * 주어진 수가 1이 될 때까지 다음 작업을 반복하면,
 * 모든 수를 1로 만들 수 있다는 추측
 * 1-1. 입력된 수가 짝수라면 2로 나눕니다. 
	1-2. 입력된 수가 홀수라면 3을 곱하고 1을 더합니다.
	2. 결과로 나온 수에 같은 작업을 1이 될 때까지 반복합니다.
 */
public class 콜라츠추측 {
	public int solution(long num) {
		int answer = 0;
	
		while(num != 1) {
			if(num % 2==0) {
				num = num/2;
			}else {
				num = num*3 + 1;
			}
			answer++;
			if(answer == 500) {
				answer = -1; 
				break;
			}
		}
		
		
		return answer;
	}
}
