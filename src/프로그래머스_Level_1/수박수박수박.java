package 프로그래머스_Level_1;
/*
 * 길이가 n이고, "수박수박수박수..."와 같은 패턴을 유지하는 문자열을 리터하는 함수
 * solution을 완성하라.
 * 예를 들어 n이 4이면 "수박수박"을 리턴하고 3이라면 "수박수"를 return
 * 
 */
public class 수박수박수박 {
	public String solution(int n) {
		String answer = "";
		
		for(int i=1; i<=n; i++) {
			if(i%2==1) {
				answer += "수";
			}else
				answer += "박";
		}
		
		
		return answer;
	}
}
